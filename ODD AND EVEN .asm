.MODEL SMALL
.STACK 100H
.DATA 
  JOYMONDAL1 DB 10,13,'ENTER NUMBER HERE :- $'
  JOYMONDAL2 DB 10,13,'ENTERED VALUE IS ODD$' 
  JOYMONDAL3 DB 10,13,'ENTERED VALUE IS EVEN$'  
.CODE 
  MAIN PROC
 
DISPLAY MACRO  MSG      
  MOV AH,9
  LEA DX,MSG
  INT 21H
  ENDM 

ASSUME CS:CODE,DS:DATA
 
  MOV AX,DATA
  MOV DS,AX

DISPLAY JOYMONDAL1

  MOV AH,1
  INT 21H
  MOV AH,0

CHECK: MOV DL,2
  DIV DL
  CMP AH,0
  JNE ODD



EVEN:
DISPLAY JOYMONDAL3
  JMP DONE   
  
ODD:
DISPLAY JOYMONDAL2

DONE:
  MOV AH,4CH
  INT 21H
  CODE ENDS
  
  MAIN ENDP
END MAIN